public class Operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        int add = a+b;
        int sub = a-b;
        int mul = a*b;
        int div = a/b;
        int rem = a%b;

        System.out.println("The Number of add is value : " +add);
        System.out.println("The Number of sub is value : " +sub);
        System.out.println("The Number of mul is value : " +mul);
        System.out.println("The Number of div is value : " +div);
        System.out.println("The Number of rem is value : " +rem);

    }
}
